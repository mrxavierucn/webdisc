<h1>¿Quienes somos?</h1>

<p>La Informática en la Universidad Católica del Norte comenzó su desarrollo en el  año 1969 en el Centro de Computación (CECUN).
    CECUN estaba orientado a la prestación de servicios internos a la UCN y servicios externos a empresas de la zona.</p>
<p>El Departamento de Ingeniería de Sistemas y Computación (DISC) fue creado el 11 de octubre de 1979,
    por el Decreto de Rectoría Nº 161/79, con el nombre de “Departamento de Ciencias de la Computación e Informática“.</p>
<p>El mencionado Departamento quedó integrado por los académicos Jornada Completa, pertenecientes a la Sección de Ingeniería de Sistemas del Departamento de Ingeniería.
    Su labor fundamental, a partir de marzo de 1980, consistió en el apoyo docente a la Carrera de Ingeniería de Ejecución en Computación e Informática,
    creada el 9 de octubre de 1979, por Decreto de Rectoría Nº 157/79.</p>
<p>El cuerpo académico inicial estaba formado por siete profesionales, de los cuales los académicos Raúl Apaz Vargas y Ricardo Durán Arriagada,
    asumieron las responsabilidades de Director de Departamento y de Jefe de Carrera, respectivamente.</p>
<p>Posteriormente, a partir de 1982, el Departamento asumió el apoyo docente a la Carrera de Ingeniería Civil en Computación e Informática.</p>
<p>Con fecha 26 de octubre de 1994, el Consejo de Departamento, acordó solicitar a las autoridades pertinentes, iniciar las gestiones para que a contar del día 1 de enero de 1995,
    se oficializara el cambio de nombre del Departamento. El nombre propuesto, Departamento de Ingeniería de Sistemas y Computación,
    permitía una mayor expresión de todas aquellas actividades que a la fecha realizaba el Área de Sistemas del Departamento.
    La anterior solicitud  fue aprobada y oficializada el 16 de diciembre de 1994, por Decreto de Rectoría Nº 114/94. A la fecha,
    desempeñaba labores de Director de Departamento, el académico Luis Lobos Flores.</p>
